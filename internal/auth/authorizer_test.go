func TestAuthorizeUsesDynamicLoopbackPKCEAndExactRedirect(t *testing.T) {
	var tokenRequest url.Values
	server := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if err := r.ParseForm(); err != nil {
			t.Fatal(err)
		}
		tokenRequest = r.Form
		w.Header().Set("Content-Type", "application/json")
		_, _ = w.Write([]byte(`{"access_token":"access-secret","token_type":"Bearer","refresh_token":"refresh-secret","expires_in":3600,"scope":"user-read-private user-library-read user-library-modify"}`))
	}))
	defer server.Close()

	var authURL *url.URL
	authorizer := Authorizer{
		HTTPClient: server.Client(),
		Endpoints:  Endpoints{AuthorizeURL: "https://accounts.example/authorize", TokenURL: server.URL},
		Timeout:    2 * time.Second,
		OpenBrowser: func(raw string) error {
			var err error
			authURL, err = url.Parse(raw)
			if err != nil {
				return err
			}
			callback := authURL.Query().Get("redirect_uri") + "?code=auth-code&state=" + url.QueryEscape(authURL.Query().Get("state"))
			go func() {
				response, getErr := http.Get(callback) // #nosec G107 -- test callback URL is generated by the local authorizer.
				if getErr == nil {
					_ = response.Body.Close()
				}
			}()
			return nil
		},
	}
	var stderr bytes.Buffer
	got, err := authorizer.Authorize(context.Background(), Request{
		ClientID: "client-id", RedirectURI: "http://127.0.0.1/callback", ErrOut: &stderr,
	})
	if err != nil {
		t.Fatal(err)
	}
	if got.AccessToken != "access-secret" || got.RefreshToken != "refresh-secret" {
		t.Fatalf("token = %+v", got)
	}
	if want := []string{ScopeUserLibraryModify, ScopeUserLibraryRead, ScopeUserReadPrivate}; !slices.Equal(got.Scopes, want) {
		t.Fatalf("scopes = %v, want %v", got.Scopes, want)
	}
	if authURL == nil || authURL.Query().Get("code_challenge_method") != "S256" ||
		authURL.Query().Get("scope") != "user-library-modify user-library-read user-read-private" {
		t.Fatalf("authorization URL = %v", authURL)
	}
	redirect := authURL.Query().Get("redirect_uri")
	if !strings.HasPrefix(redirect, "http://127.0.0.1:") || !strings.HasSuffix(redirect, "/callback") {
		t.Fatalf("dynamic redirect = %q", redirect)
	}
	if tokenRequest.Get("redirect_uri") != redirect || tokenRequest.Get("client_id") != "client-id" || tokenRequest.Get("code") != "auth-code" {
		t.Fatalf("token request = %v", tokenRequest)
	}
	if oauth2.S256ChallengeFromVerifier(tokenRequest.Get("code_verifier")) != authURL.Query().Get("code_challenge") {
		t.Fatal("token exchange verifier does not match authorization challenge")
	}
	if !strings.Contains(stderr.String(), authURL.String()) {
		t.Fatalf("stderr did not contain authorization URL: %q", stderr.String())
	}
}

