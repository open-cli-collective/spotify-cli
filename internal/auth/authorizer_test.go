func TestAuthorizeCancellationClosesListener(t *testing.T) {
	ctx, cancel := context.WithCancel(context.Background())
	var address string
	authorizer := Authorizer{
		Endpoints: Endpoints{AuthorizeURL: "https://accounts.example/authorize", TokenURL: "https://accounts.example/token"},
		OpenBrowser: func(raw string) error {
			authURL, err := url.Parse(raw)
			if err != nil {
				return err
			}
			redirect, err := url.Parse(authURL.Query().Get("redirect_uri"))
			if err != nil {
				return err
			}
			address = redirect.Host
			cancel()
			return nil
		},
	}
	started := time.Now()
	_, err := authorizer.Authorize(ctx, Request{
		ClientID: "client-id", RedirectURI: "http://127.0.0.1/callback", ErrOut: io.Discard,
	})
	if !errors.Is(err, context.Canceled) || time.Since(started) > time.Second {
		t.Fatalf("error = %v after %s", err, time.Since(started))
	}
	listener, err := net.Listen("tcp4", address) // #nosec G102 -- verifies that the test loopback listener was closed.
	if err != nil {
		t.Fatalf("callback listener remained open: %v", err)
	}
	_ = listener.Close()
}

type authURLWriter struct {
	mu   sync.Mutex
	urls chan<- string
}

func (writer *authURLWriter) Write(data []byte) (int, error) {
	writer.mu.Lock()
	defer writer.mu.Unlock()
	for _, line := range strings.Split(string(data), "\n") {
		line = strings.TrimSpace(strings.TrimPrefix(line, "Authorization URL:"))
		if strings.HasPrefix(line, "https://accounts.example/authorize") {
			select {
			case writer.urls <- line:
			default:
			}
		}
	}
	return len(data), nil
}
