func TestAuthorizeReportsDenialWithoutLeakingCallbackValues(t *testing.T) {
	const denialSentinel = "denial-value-sentinel"
	responses := make(chan string, 1)
	authorizer := Authorizer{
		Endpoints: Endpoints{AuthorizeURL: "https://accounts.example/authorize", TokenURL: "https://accounts.example/token"},
		Timeout:   2 * time.Second,
		OpenBrowser: func(raw string) error {
			authURL, err := url.Parse(raw)
			if err != nil {
				return err
			}
			callback := authURL.Query().Get("redirect_uri") + "?error=" + denialSentinel + "&state=" + url.QueryEscape(authURL.Query().Get("state"))
			go func() {
				response, getErr := http.Get(callback) // #nosec G107 -- test callback URL is generated by the local authorizer.
				if getErr != nil {
					responses <- getErr.Error()
					return
				}
				defer func() { _ = response.Body.Close() }()
				body, _ := io.ReadAll(response.Body)
				responses <- string(body)
			}()
			return nil
		},
	}
	_, err := authorizer.Authorize(context.Background(), Request{
		ClientID: "client-id", RedirectURI: "http://127.0.0.1/callback", ErrOut: io.Discard,
	})
	if !errors.Is(err, ErrAccessDenied) {
		t.Fatalf("error = %v, want ErrAccessDenied", err)
	}
	if strings.Contains(err.Error(), denialSentinel) || strings.Contains(<-responses, denialSentinel) {
		t.Fatal("authorization denial leaked callback value")
	}
}

