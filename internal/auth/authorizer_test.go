func TestAuthorizeExchangeFailureDoesNotLeakCodeOrVerifier(t *testing.T) {
	const code = "authorization-code-sentinel"
	var verifier string
	server := httptest.NewServer(http.HandlerFunc(func(writer http.ResponseWriter, request *http.Request) {
		if err := request.ParseForm(); err != nil {
			t.Error(err)
		}
		verifier = request.Form.Get("code_verifier")
		writer.Header().Set("Content-Type", "application/json")
		writer.WriteHeader(http.StatusBadRequest)
		_, _ = fmt.Fprintf(writer, `{"error":"invalid_grant","error_description":"echo %s %s"}`, code, verifier)
	}))
	defer server.Close()

	reader, writer := io.Pipe()
	urls := make(chan string, 1)
	var stderr bytes.Buffer
	result := make(chan error, 1)
	authorizer := Authorizer{
		HTTPClient: server.Client(),
		Endpoints:  Endpoints{AuthorizeURL: "https://accounts.example/authorize", TokenURL: server.URL},
	}
	go func() {
		_, err := authorizer.Authorize(context.Background(), Request{
			ClientID: "client-id", RedirectURI: "https://callback.example/spotify", AuthCodeStdin: true,
			In: reader, ErrOut: io.MultiWriter(&stderr, &authURLWriter{urls: urls}),
		})
		result <- err
	}()
	authURL, err := url.Parse(<-urls)
	if err != nil {
		t.Fatal(err)
	}
	callback := authURL.Query().Get("redirect_uri") + "?code=" + code + "&state=" + url.QueryEscape(authURL.Query().Get("state"))
	_, _ = io.WriteString(writer, callback)
	_ = writer.Close()
	err = <-result
	if !errors.Is(err, ErrExchange) {
		t.Fatalf("error = %v, want ErrExchange", err)
	}
	combined := stderr.String() + err.Error()
	if verifier == "" || strings.Contains(combined, code) || strings.Contains(combined, verifier) {
		t.Fatalf("exchange secret leaked: %q", combined)
	}
}

